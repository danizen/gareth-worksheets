/* Statements
 * ----------
 * Statements are lines of code that do something.
 * Statements end with a semicolon, e.g. ";"
 */

// A declaration statement might declare a variable x
var x = 2;

// Another statement might do some math (in algebraic notation - '*' means multiplication.
var y = 3*x + 4;

// Another statement might call a function (this one prints)
console.log('The value of y = '+y);		// '+' with a string just adds to the string

// All of these end with a semicolon
